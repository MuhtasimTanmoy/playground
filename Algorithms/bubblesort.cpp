void bubble_sort(int *a, int size)
{
	for (int i = 0; i < size; i++)
	{
		for (int j = 0; j < size - 1; j++)
		{
			if (a[j] > a[j + 1])
			{
				swap(a[j], a[j + 1]);
			}
		}
	}
}

void solve()
{
	int n;
	cin >> n;
	int li[n];
	for (int i = 0; i < n; i++)
	{
		cin >> li[i];
	}

	bubble_sort(li, n);

	for (int i = 0; i < n; i++)
	{
		cout << li[i] << " ";
	}
}

